public class Continhas{
    public static void main(String args[]){
        //Declarar variavel, ou seja, reservar espaço na memória
        //Tipo inteiro e posso declarar uma por linha ou mais de uma na mesma linha
        int a, b;
        int c;
        //Atribuir valor fixo para as variaveis
        a = 19;
        b = 3;
        //Execucao da soma
        c = a + b;
        //Exibir valores na tela para as tres variaveis
        System.out.println("Valor do a = " + a);
        System.out.println("Valor do b = " + b);
        System.out.println("Valor da soma = " + c);
        //Execucao da subtracao
        c = a - b;
        //Exibir valores na tela para as tres variaveis
        System.out.println("Valor da subtracao = " + c);
        //Execucao da multiplicacao
        c = a * b;
        //Exibir valores na tela para as tres variaveis
        System.out.println("Valor da multiplicacao = " + c);
        //Execucao da divisao
        c = a / b;
        //Exibir valores na tela para as tres variaveis
        System.out.println("Valor da divisao = " + c);
        //Execucao do resto
        c = a % b;
        //Exibir valores na tela para as tres variaveis
        System.out.println("Valor do resto da divisao = " + c);
        //Variavel do tipo double, casas decimais
        double x;
        x = 1.0/2;
        System.out.println("Valor da divisao com tipo double = " + x);
    }
}